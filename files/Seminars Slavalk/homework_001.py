# Напишите программу, которая принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день
# выходным.
#
# Пример:
#
# - 6 -> да
# - 7 -> да
# - 1 -> нет

a = int(input('введите номер дня недели: '))

if a == 7 or a == 6:
	print('да')
elif 1 <= a <= 5:
	print('нет')
else:
	print('это не номер дня недели')

