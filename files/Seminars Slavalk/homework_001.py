# Напишите программу, которая принимает на вход координаты точки (X и Y), причём X ≠ 0 и Y ≠ 0 и выдаёт номер
# четверти плоскости, в которой находится эта точка (или на какой оси она находится).
#
# Пример:
#
# - x=34; y=-30 -> 4
# - x=2; y=4-> 1
# - x=-34; y=-30 -> 3

n, m = (map(int, input('введите координаты точки через пробел (пример: -4 5) ').split()))

try:
	n = float(n)
	m = float(m)
	if n > 0 and m > 0:
		print('1')
	elif n < 0 and m > 0:
		print('2')
	elif n < 0 and m < 0:
		print('3')
	elif n > 0 and m < 0:
		print('4')
	elif n == 0 and m != 0:
		print('лежит на оси Y')
	elif m == 0 and n != 0:
		print('лежит на оси X')
	else:
		print('лежит на пересечении осей X и Y')
except:
	print('не корректный ввод')

