    try:
        n = float(n)
        m = float(m)
        if n > 0 and m > 0:
            print('1')
        elif n < 0 and m > 0:
            print('2')
        elif n < 0 and m < 0:
            print('3')
        elif n > 0 and m < 0:
            print('4')
        else:
            if n == 0 and m != 0:
                print('лежит на оси Y')
            elif m == 0 and n != 0:
                print('лежит на оси X')
            else:
                print('лежит на пересечении осей X и Y')
    except:
        print('не корректный ввод')


