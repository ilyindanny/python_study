# Напишите программу, которая принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день выходным.

# Пример:

# - 6 -> да
# - 7 -> да
# - 1 -> нет

def IsDayOff(n):
    try:
        n = int(n)
        if n == 6 or n == 7:
            return "Да"
        else:
            return 'нет'
    except:
        return 'не корректный ввод'


print(IsDayOff((input('введите цифру, обозначающую день недели: '))))


